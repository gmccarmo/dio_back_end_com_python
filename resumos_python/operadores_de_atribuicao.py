# Atribuição simples
saldo = 500
print(saldo) # Saída: 500

# Atribuição com adição
saldo = 500
saldo += 200 # Equivalente a saldo = saldo + 200
print(saldo) # Saída: 700

# Atribuição com subtração
saldo = 500
saldo -= 100 # Equivalente a saldo = saldo - 100
print(saldo) # Saída: 400

# Atribuição com multiplicação
saldo = 500
saldo *= 2 # Equivalente a saldo = saldo * 2
print(saldo) # Saída: 1000

# Atribuição com divisão
saldo = 500
saldo /= 5 # Equivalente a saldo = saldo / 5
print(saldo) # Saída: 100.0

# Atribuição com divisão inteira
saldo = 500
saldo //= 5 # Equivalente a saldo = saldo // 5
print(saldo) # Saída: 100

# Atribuição com módulo
saldo = 500
saldo %= 480 # Equivalente a saldo = saldo % 480
print(saldo) # Saída: 20

# Atribuição com exponenciação
saldo = 80
saldo **= 2 # Equivalente a saldo = saldo ** 2
print(saldo) # Saída: 6400

