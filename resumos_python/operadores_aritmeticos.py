# Adição
print(1 + 1) # Saída: 2

# Subtração
print(10 - 2) # Saída: 8

# Multiplicação
print(4 * 3) # Saída: 12

# Divisão
print (12 / 3) # Saída: 4.0

# Divisão inteira
print(12 // 2) # Saída: 6

# Módulo (resto da divisão)
print(10 % 3) # Saída: 1

# Exponenciação
print(2 ** 3) # Saída: 8

'''
# Precedência dos operadores

A definição indica a seguinte ordem como a correta:
1. Parênteses
2. Exponenciação
3. Multiplicação e Divisão (da esquerda para a direita)
4. Adição e Subtração (da esquerda para a direita)
'''
# Exemplos precedência

print ( 10 - 5 * 2 ) # Saída: 0

print ( (10 - 5) * 2 ) # Saída: 10

print (10 ** 2 * 2) # Saída: 200

print (10 ** (2 * 2)) # Saída: 10000

print (10 / 2 * 4) # Saída: 20.0

