# Igualdade ==
saldo = 450
saque = 200
print(saldo == saque) # Saída: False

# Diferença !=
saldo = 450
saque = 200
print(saldo != saque) # Saída: True

# Maior que / maior ou igual que
saldo = 450
saque = 200
print(saldo > saque) # Saída: True
print(saldo >= saque) # Saída: True

# Menor que / menor ou igual que
saldo = 450
saque = 200
print(saldo < saque) # Saída: False
print(saldo <= saque) # Saída: False

